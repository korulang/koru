    fn serializeEventDeclJson(self: *AstSerializer, event: *const ast.EventDecl) !void {
        // Event name
        try self.writeIndent();
        try self.write("\"name\": ");
        if (event.path.segments.len > 0) {
            try self.writeString(event.path.segments[event.path.segments.len - 1]);
        } else {
            try self.writeString("");
        }
        try self.write(",\n");

        // Path
        try self.writeIndent();
        try self.write("\"path\": ");
        try self.serializeDottedPathJson(&event.path);
        try self.write(",\n");

        // Module
        try self.writeIndent();
        try self.write("\"module\": ");
        try self.writeString(event.module);
        try self.write(",\n");

        // Input shape
        try self.writeIndent();
        try self.write("\"input\": ");
