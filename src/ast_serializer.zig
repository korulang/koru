const std = @import("std");
const ast = @import("ast");
const Parser = @import("parser").Parser;  // Needed for tests

// Define explicit error set to avoid circular inference issues
const SerializeError = error{
    OutOfMemory,
};


/// Serializes a parsed Koru AST into Zig source code containing the AST as data.
/// This is Phase 2 of the metacircular compilation architecture.
/// 
/// The output is a Zig file containing:
/// 1. Type definitions for all AST nodes
/// 2. The complete program AST as const declarations
/// 3. All Zig code preserved as string literals
pub const AstSerializer = struct {
    allocator: std.mem.Allocator,
    buffer: std.ArrayList(u8),
    indent_level: usize,

    const indent_size = 4;

    pub fn init(allocator: std.mem.Allocator) !AstSerializer {
        return AstSerializer{
            .allocator = allocator,
            .buffer = try std.ArrayList(u8).initCapacity(allocator, 4096),
            .indent_level = 0,
        };
    }

    pub fn deinit(self: *AstSerializer) void {
        self.buffer.deinit(self.allocator);
    }

    fn indent(self: *AstSerializer) void {
        self.indent_level += 1;
    }

    fn dedent(self: *AstSerializer) void {
        if (self.indent_level > 0) {
            self.indent_level -= 1;
        }
    }

    fn writeIndent(self: *AstSerializer) SerializeError!void {
        for (0..self.indent_level * indent_size) |_| {
            try self.buffer.append(self.allocator, ' ');
        }
    }

    fn write(self: *AstSerializer, text: []const u8) SerializeError!void {
        try self.buffer.appendSlice(self.allocator, text);
    }

    fn writeLine(self: *AstSerializer, text: []const u8) SerializeError!void {
        try self.writeIndent();
        try self.write(text);
        try self.write("\n");
    }

    fn writeString(self: *AstSerializer, str: []const u8) SerializeError!void {
        try self.write("\"");
        // Escape special characters in the string
        for (str) |c| {
            switch (c) {
                '\n' => try self.write("\\n"),
                '\r' => try self.write("\\r"),
                '\t' => try self.write("\\t"),
                '\\' => try self.write("\\\\"),
                '"' => try self.write("\\\""),
                else => try self.buffer.append(self.allocator, c),
            }
        }
        try self.write("\"");
    }

    fn writeMultilineString(self: *AstSerializer, str: []const u8) !void {
        // Use Zig's multiline string syntax for better readability
        try self.write("\n");
        self.indent();
        try self.writeIndent();
        try self.write("\\\\");
        
        var iter = std.mem.splitScalar(u8, str, '\n');
        var first = true;
        while (iter.next()) |line| {
            if (!first) {
                try self.write("\n");
                try self.writeIndent();
                try self.write("\\\\");
            }
            try self.write(line);
            first = false;
        }
        
        // Close the multiline string properly
        try self.write("\n");
        try self.writeIndent();
        
        self.dedent();
    }

    /// Serialize a complete Program (the root AST node)
    pub fn serialize(self: *AstSerializer, source_file: *const ast.Program) ![]const u8 {
        // Write header comment
        try self.writeLine("// Generated by Koru AST Serializer");
        try self.writeLine("// This file contains the complete program AST as Zig data structures");
        try self.writeLine("");

        // Write type definitions
        try self.writeTypeDefinitions();
        try self.writeLine("");
        
        // Define ProgramAST type alias for compiler events
        try self.writeLine("// ProgramAST is the type used by compiler events to reference the AST");
        try self.writeLine("const ProgramAST = *const Program;");
        try self.writeLine("");

        // Write the program AST
        try self.writeLine("pub const PROGRAM_AST = Program{");
        self.indent();

        // Write items array
        try self.writeLine(".items = &[_]Item{");
        self.indent();

        for (source_file.items) |item| {
            try self.serializeItem(&item);
            try self.write(",\n");
        }

        self.dedent();
        try self.writeLine("},");

        // Write module_annotations array
        try self.writeIndent();
        try self.write(".module_annotations = &[_][]const u8{");
        if (source_file.module_annotations.len > 0) {
            for (source_file.module_annotations, 0..) |annotation, i| {
                if (i > 0) try self.write(", ");
                try self.writeString(annotation);
            }
        }
        try self.write("},\n");

        // Write main_module_name
        try self.writeIndent();
        try self.write(".main_module_name = ");
        try self.writeString(source_file.main_module_name);
        try self.write(",\n");

        try self.writeLine(".allocator = undefined,");

        self.dedent();
        try self.writeLine("};");
        try self.writeLine("");

        // Generate helper function for safely deinit'ing transformed ASTs
        try self.writeLine("/// Helper to safely deinit transformed ASTs while preserving PROGRAM_AST");
        try self.writeLine("fn maybeDeinitAst(source_ast: *const Program) void {");
        self.indent();
        try self.writeLine("if (source_ast == &PROGRAM_AST) {");
        self.indent();
        try self.writeLine("// This is the const PROGRAM_AST - don't deinit");
        try self.writeLine("return;");
        self.dedent();
        try self.writeLine("}");
        try self.writeLine("// Heap-allocated transformed AST - safe to deinit");
        try self.writeLine("var mutable_ast = @constCast(source_ast);");
        try self.writeLine("mutable_ast.deinit();");
        self.dedent();
        try self.writeLine("}");

        return try self.buffer.toOwnedSlice(self.allocator);
    }

    /// Serialize a Program to JSON format
    pub fn serializeToJson(self: *AstSerializer, source_file: *const ast.Program) ![]const u8 {
        try self.write("{\n");
        self.indent();

        // Output module_annotations first
        try self.writeIndent();
        try self.write("\"module_annotations\": [\n");
        self.indent();
        for (source_file.module_annotations, 0..) |ann, i| {
            if (i > 0) {
                try self.write(",\n");
            }
            try self.writeIndent();
            try self.write("\"");
            try self.write(ann);
            try self.write("\"");
        }
        try self.write("\n");
        self.dedent();
        try self.writeIndent();
        try self.write("],\n");

        // Output items
        try self.writeIndent();
        try self.write("\"items\": [\n");
        self.indent();

        for (source_file.items, 0..) |*item, i| {
            if (i > 0) {
                try self.write(",\n");
            }
            try self.serializeItemJson(item);
        }

        try self.write("\n");
        self.dedent();
        try self.writeIndent();
        try self.write("]\n");
        self.dedent();
        try self.write("}\n");

        return try self.buffer.toOwnedSlice(self.allocator);
    }

    fn writeTypeDefinitions(self: *AstSerializer) !void {
        try self.writeLine("// AST Type Definitions - imported from ast module");
        try self.writeLine("const ast = @import(\"ast\");");
        try self.writeLine("");

        // Create local aliases for all AST types used in serialization
        try self.writeLine("// Type aliases for convenience");
        try self.writeLine("const Program = ast.Program;");
        try self.writeLine("const Item = ast.Item;");
        try self.writeLine("const HostLine = ast.HostLine;");
        try self.writeLine("const EventDecl = ast.EventDecl;");
        try self.writeLine("const HostTypeDecl = ast.HostTypeDecl;");
        try self.writeLine("const ProcDecl = ast.ProcDecl;");
        try self.writeLine("const Flow = ast.Flow;");
        try self.writeLine("const EventTap = ast.EventTap;");
        try self.writeLine("const LabelDecl = ast.LabelDecl;");
        try self.writeLine("const SubflowImpl = ast.SubflowImpl;");
        try self.writeLine("const SubflowBody = ast.SubflowBody;");
        try self.writeLine("const ImportDecl = ast.ImportDecl;");
        try self.writeLine("const ModuleDecl = ast.ModuleDecl;");
        try self.writeLine("const DottedPath = ast.DottedPath;");
        try self.writeLine("const Shape = ast.Shape;");
        try self.writeLine("const Field = ast.Field;");
        try self.writeLine("const Branch = ast.Branch;");
        try self.writeLine("const BranchVariant = ast.BranchVariant;");
        try self.writeLine("const SuperShape = ast.SuperShape;");
        try self.writeLine("const Invocation = ast.Invocation;");
        try self.writeLine("const Continuation = ast.Continuation;");
        try self.writeLine("const BindingType = ast.BindingType;");
        try self.writeLine("const Step = ast.Step;");
        try self.writeLine("const Arg = ast.Arg;");
        try self.writeLine("const BranchConstructor = ast.BranchConstructor;");
        try self.writeLine("const Expression = ast.Expression;");
        try self.writeLine("const SourceLocation = ast.SourceLocation;");
        try self.writeLine("const Source = ast.Source;");
        try self.writeLine("const CapturedExpression = ast.CapturedExpression;");
        try self.writeLine("const CapturedScope = ast.CapturedScope;");
        try self.writeLine("const ScopeBinding = ast.ScopeBinding;");
    }

    fn serializeItem(self: *AstSerializer, item: *const ast.Item) !void {
        try self.writeIndent();
        switch (item.*) {
            .module_decl => |module| {
                try self.write(".{ .module_decl = ");
                try self.serializeModuleDecl(&module);
                try self.write(" }");
            },
            .host_line => |line| {
                try self.write(".{ .host_line = .{ .content = ");
                try self.writeString(line.content);
                try self.write(", .location = ");
                try self.serializeSourceLocation(&line.location);
                try self.write(", .module = ");
                try self.writeString(line.module);
                try self.write(" } }");
            },
            .event_decl => |event| {
                try self.write(".{ .event_decl = ");
                try self.serializeEventDecl(&event);
                try self.write(" }");
            },
            .proc_decl => |proc| {
                try self.write(".{ .proc_decl = ");
                try self.serializeProcDecl(&proc);
                try self.write(" }");
            },
            .flow => |flow| {
                try self.write(".{ .flow = ");
                try self.serializeFlow(&flow);
                try self.write(" }");
            },
            .import_decl => |import| {
                try self.write(".{ .import_decl = ");
                try self.serializeImportDecl(&import);
                try self.write(" }");
            },
            .subflow_impl => |subflow| {
                try self.write(".{ .subflow_impl = ");
                try self.serializeSubflowImpl(&subflow);
                try self.write(" }");
            },
            .label_decl => |label| {
                try self.write(".{ .label_decl = ");
                try self.serializeLabelDecl(&label);
                try self.write(" }");
            },
            .event_tap => |tap| {
                try self.write(".{ .event_tap = ");
                try self.serializeEventTap(&tap);
                try self.write(" }");
            },
            .host_type_decl => |host_type| {
                try self.write(".{ .host_type_decl = ");
                try self.serializeHostTypeDecl(&host_type);
                try self.write(" }");
            },
            .parse_error => |error_node| {
                // Parse errors shouldn't appear in Zig code generation
                // This is only used for --ast-json mode for IDE tooling
                try self.write(".{ .parse_error = .{ .error_code = .");
                try self.write(@tagName(error_node.error_code));
                try self.write(", .message = ");
                try self.writeString(error_node.message);
                try self.write(", .location = ");
                try self.serializeSourceLocation(&error_node.location);
                try self.write(", .raw_text = ");
                try self.writeString(error_node.raw_text);
                try self.write(", .hint = ");
                if (error_node.hint) |hint| {
                    try self.writeString(hint);
                } else {
                    try self.write("null");
                }
                try self.write(" } }");
            },

            // IR nodes (created by optimizer - should not appear in backend.zig serialization)
            // These get emitted directly by visitor_emitter.zig instead
            .native_loop => {
                // NativeLoop IR nodes should be handled by visitor_emitter, not serialized to backend.zig
                try self.write(".{ .native_loop = /* IR node - handled by emitter */ }");
            },
            .fused_event => {
                // FusedEvent IR nodes should be handled by visitor_emitter, not serialized to backend.zig
                try self.write(".{ .fused_event = /* IR node - handled by emitter */ }");
            },
            .inlined_event => {
                // InlinedEvent IR nodes should be handled by visitor_emitter, not serialized to backend.zig
                try self.write(".{ .inlined_event = /* IR node - handled by emitter */ }");
            },
            .inline_code => {
                // InlineCode IR nodes should be handled by visitor_emitter, not serialized to backend.zig
                try self.write(".{ .inline_code = /* IR node - handled by emitter */ }");
            },
        }
    }

    fn serializeModuleDecl(self: *AstSerializer, module: *const ast.ModuleDecl) SerializeError!void {
        try self.write(".{ .logical_name = ");
        try self.writeString(module.logical_name);
        try self.write(", .canonical_path = ");
        try self.writeString(module.canonical_path);
        try self.write(", .items = &[_]Item{");
        self.indent();
        for (module.items) |*item| {
            try self.writeLine("");
            try self.writeIndent();
            try self.write("            ");
            try self.serializeItem(item);
            try self.write(",");
        }
        self.dedent();
        if (module.items.len > 0) {
            try self.writeLine("");
            try self.writeIndent();
            try self.write("        ");
        }
        try self.write("}, .is_system = ");
        try self.write(if (module.is_system) "true" else "false");
        try self.write(", .annotations = &.{");
        for (module.annotations, 0..) |ann, i| {
            if (i > 0) try self.write(", ");
            try self.writeString(ann);
        }
        try self.write("}");
        try self.write(", .location = ");
        try self.serializeSourceLocation(module.location);
        try self.write(" }");
    }

    fn serializeEventDecl(self: *AstSerializer, event: *const ast.EventDecl) !void {
        try self.write("EventDecl{\n");
        self.indent();
        
        // Path
        try self.writeIndent();
        try self.write(".path = ");
        try self.serializeDottedPath(event.path);
        try self.write(",\n");
        
        // Input shape
        try self.writeIndent();
        try self.write(".input = ");
        try self.serializeShape(&event.input);
        try self.write(",\n");
        
        // Branches
        try self.writeIndent();
        try self.write(".branches = &[_]Branch{\n");
        self.indent();
        for (event.branches) |branch| {
            try self.serializeBranch(&branch);
            try self.write(",\n");
        }
        self.dedent();
        try self.writeIndent();
        try self.write("},\n");

        // Is public
        try self.writeIndent();
        try self.write(".is_public = ");
        try self.write(if (event.is_public) "true" else "false");
        try self.write(",\n");
        
        // Is implicit flow
        try self.writeIndent();
        try self.write(".is_implicit_flow = ");
        try self.write(if (event.is_implicit_flow) "true" else "false");
        try self.write(",\n");

        // Is abstract
        try self.writeIndent();
        try self.write(".is_abstract = ");
        try self.write(if (event.is_abstract) "true" else "false");
        try self.write(",\n");

        // Annotations
        try self.writeIndent();
        try self.write(".annotations = &.{\n");
        self.indent();
        for (event.annotations) |ann| {
            try self.writeIndent();
            try self.writeString(ann);
            try self.write(",\n");
        }
        self.dedent();
        try self.writeIndent();
        try self.write("},\n");

        // Location
        try self.writeIndent();
        try self.write(".location = ");
        try self.serializeSourceLocation(&event.location);
        try self.write(",\n");

        // Module
        try self.writeIndent();
        try self.write(".module = ");
        try self.writeString(event.module);
        try self.write(",\n");

        self.dedent();
        try self.writeIndent();
        try self.write("}");
    }

    fn serializeProcDecl(self: *AstSerializer, proc: *const ast.ProcDecl) !void {
        try self.write("ProcDecl{\n");
        self.indent();
        
        // Path
        try self.writeIndent();
        try self.write(".path = ");
        try self.serializeDottedPath(proc.path);
        try self.write(",\n");
        
        // Body (Zig code as multiline string)
        try self.writeIndent();
        try self.write(".body = ");
        
        if (std.mem.indexOf(u8, proc.body, "\n") != null) {
            try self.writeMultilineString(proc.body);
            try self.write(",\n");
        } else {
            try self.writeString(proc.body);
            try self.write(",\n");
        }
        
        // Serialize inline flows
        try self.writeIndent();
        try self.write(".inline_flows = &[_]Flow{\n");
        self.indent();
        for (proc.inline_flows) |*flow| {
            try self.serializeFlow(flow);
            try self.write(",\n");
        }
        self.dedent();
        try self.writeIndent();
        try self.write("},\n");
        
        // Serialize annotations
        try self.writeIndent();
        try self.write(".annotations = &.{\n");
        self.indent();
        for (proc.annotations) |ann| {
            try self.writeIndent();
            try self.writeString(ann);
            try self.write(",\n");
        }
        self.dedent();
        try self.writeIndent();
        try self.write("},\n");

        // Serialize target
        try self.writeIndent();
        if (proc.target) |target| {
            try self.write(".target = ");
            try self.writeString(target);
            try self.write(",\n");
        } else {
            try self.write(".target = null,\n");
        }

        // Serialize is_impl flag
        try self.writeIndent();
        try self.write(".is_impl = ");
        try self.write(if (proc.is_impl) "true" else "false");
        try self.write(",\n");

        // Purity tracking
        try self.writeIndent();
        try self.write(".is_pure = ");
        try self.write(if (proc.is_pure) "true" else "false");
        try self.write(",\n");

        try self.writeIndent();
        try self.write(".is_transitively_pure = ");
        try self.write(if (proc.is_transitively_pure) "true" else "false");
        try self.write(",\n");

        // Location
        try self.writeIndent();
        try self.write(".location = ");
        try self.serializeSourceLocation(&proc.location);
        try self.write(",\n");

        // Module
        try self.writeIndent();
        try self.write(".module = ");
        try self.writeString(proc.module);
        try self.write(",\n");

        self.dedent();
        try self.writeIndent();
        try self.write("}");
    }

    fn serializeFlow(self: *AstSerializer, flow: *const ast.Flow) SerializeError!void {
        try self.write("Flow{\n");
        self.indent();
        
        // Invocation
        try self.writeIndent();
        try self.write(".invocation = ");
        try self.serializeInvocation(&flow.invocation);
        try self.write(",\n");
        
        // Continuations
        try self.writeIndent();
        try self.write(".continuations = &[_]Continuation{\n");
        self.indent();
        for (flow.continuations) |cont| {
            try self.serializeContinuation(&cont);
            try self.write(",\n");
        }
        self.dedent();
        try self.writeIndent();
        try self.write("},\n");
        
        // Pre-label (always write, even if null)
        try self.writeIndent();
        try self.write(".pre_label = ");
        if (flow.pre_label) |label| {
            try self.writeString(label);
        } else {
            try self.write("null");
        }
        try self.write(",\n");
        
        // Post-label (always write, even if null)
        try self.writeIndent();
        try self.write(".post_label = ");
        if (flow.post_label) |label| {
            try self.writeString(label);
        } else {
            try self.write("null");
        }
        try self.write(",\n");
        
        // SuperShape for inline flows with union types
        try self.writeIndent();
        try self.write(".super_shape = ");
        if (flow.super_shape) |ss| {
            try self.serializeSuperShape(&ss);
        } else {
            try self.write("null");
        }
        try self.write(",\n");

        // Purity tracking
        try self.writeIndent();
        try self.write(".is_pure = ");
        try self.write(if (flow.is_pure) "true" else "false");
        try self.write(",\n");

        try self.writeIndent();
        try self.write(".is_transitively_pure = ");
        try self.write(if (flow.is_transitively_pure) "true" else "false");
        try self.write(",\n");

        // Location
        try self.writeIndent();
        try self.write(".location = ");
        try self.serializeSourceLocation(&flow.location);
        try self.write(",\n");

        // Module
        try self.writeIndent();
        try self.write(".module = ");
        try self.writeString(flow.module);
        try self.write(",\n");

        self.dedent();
        try self.writeIndent();
        try self.write("}");
    }

    fn serializeSuperShape(self: *AstSerializer, ss: *const ast.SuperShape) SerializeError!void {
        try self.write("SuperShape{ .branches = &[_]SuperShape.BranchVariant{\n");
        self.indent();

        for (ss.branches) |branch| {
            try self.writeIndent();
            try self.write("SuperShape.BranchVariant{\n");
            self.indent();
            
            try self.writeIndent();
            try self.write(".name = ");
            try self.writeString(branch.name);
            try self.write(",\n");
            
            try self.writeIndent();
            try self.write(".payload = ");
            try self.serializeShape(&branch.payload);
            try self.write(",\n");
            
            try self.writeIndent();
            try self.write(".sources = &[_]DottedPath{\n");
            self.indent();
            for (branch.sources) |source| {
                try self.writeIndent();
                try self.serializeDottedPath(source);
                try self.write(",\n");
            }
            self.dedent();
            try self.writeIndent();
            try self.write("},\n");

            self.dedent();
            try self.writeIndent();
            try self.write("},\n");
        }

        self.dedent();
        try self.writeIndent();
        try self.write("} }");
    }

    fn serializeEventTap(self: *AstSerializer, tap: *const ast.EventTap) SerializeError!void {
        try self.write("EventTap{\n");
        self.indent();
        
        // Source (nullable)
        try self.writeIndent();
        try self.write(".source = ");
        if (tap.source) |source| {
            try self.serializeDottedPath(source);
        } else {
            try self.write("null");
        }
        try self.write(",\n");

        // Destination (nullable)
        try self.writeIndent();
        try self.write(".destination = ");
        if (tap.destination) |dest| {
            try self.serializeDottedPath(dest);
        } else {
            try self.write("null");
        }
        try self.write(",\n");
        
        // Continuations
        try self.writeIndent();
        try self.write(".continuations = &[_]Continuation{\n");
        self.indent();
        for (tap.continuations) |cont| {
            try self.serializeContinuation(&cont);
            try self.write(",\n");
        }
        self.dedent();
        try self.writeIndent();
        try self.write("},\n");
        
        // is_input_tap
        try self.writeIndent();
        try self.write(".is_input_tap = ");
        if (tap.is_input_tap) {
            try self.write("true");
        } else {
            try self.write("false");
        }
        try self.write(",\n");

        // Annotations
        try self.writeIndent();
        try self.write(".annotations = &.{\n");
        self.indent();
        for (tap.annotations) |ann| {
            try self.writeIndent();
            try self.writeString(ann);
            try self.write(",\n");
        }
        self.dedent();
        try self.writeIndent();
        try self.write("},\n");

        try self.writeIndent();
        try self.write(".location = ");
        try self.serializeSourceLocation(tap.location);
        try self.write(",\n");

        try self.writeIndent();
        try self.write(".module = ");
        try self.writeString(tap.module);
        try self.write(",\n");

        self.dedent();
        try self.writeIndent();
        try self.write("}");
    }

    fn serializeHostTypeDecl(self: *AstSerializer, host_type: *const ast.HostTypeDecl) SerializeError!void {
        try self.write("HostTypeDecl{\n");
        self.indent();

        // Name
        try self.writeIndent();
        try self.write(".name = ");
        try self.writeString(host_type.name);
        try self.write(",\n");

        // Shape
        try self.writeIndent();
        try self.write(".shape = ");
        try self.serializeShape(&host_type.shape);
        try self.write(",\n");

        self.dedent();
        try self.writeIndent();
        try self.write("}");
