    fn serializeContinuation(self: *AstSerializer, cont: *const ast.Continuation) SerializeError!void {
        try self.writeIndent();
        try self.write("Continuation{\n");
        self.indent();
        
        // Branch name
        try self.writeIndent();
        try self.write(".branch = ");
        try self.writeString(cont.branch);
        try self.write(",\n");
        
        // Binding (always write, even if null)
        try self.writeIndent();
        try self.write(".binding = ");
        if (cont.binding) |bind| {
            try self.writeString(bind);
        } else {
            try self.write("null");
        }
        try self.write(",\n");

        // Binding type
        try self.writeIndent();
        try self.write(".binding_type = .");
        try self.write(@tagName(cont.binding_type));
        try self.write(",\n");

        // Binding annotations (e.g., [mutable])
        try self.writeIndent();
        try self.write(".binding_annotations = &[_][]const u8{");
        if (cont.binding_annotations.len > 0) {
            try self.write("\n");
            self.indent();
            for (cont.binding_annotations) |ann| {
                try self.writeIndent();
                try self.writeString(ann);
                try self.write(",\n");
            }
            self.dedent();
            try self.writeIndent();
        }
        try self.write("},\n");

        // Catch-all flag
        try self.writeIndent();
        try self.write(".is_catchall = ");
        try self.write(if (cont.is_catchall) "true" else "false");
        try self.write(",\n");

        // Catch-all metatype
        try self.writeIndent();
        try self.write(".catchall_metatype = ");
        if (cont.catchall_metatype) |meta| {
            try self.writeString(meta);
        } else {
            try self.write("null");
        }
        try self.write(",\n");

        // Condition (where clause)
        try self.writeIndent();
        try self.write(".condition = ");
        if (cont.condition) |cond| {
            try self.writeString(cond);
        } else {
            try self.write("null");
        }
        try self.write(",\n");
        
        // Condition expression (parsed where clause - for now just null)
        try self.writeIndent();
        try self.write(".condition_expr = null,\n");
        
        // Node
        try self.writeIndent();
        if (cont.node) |step| {
            try self.write(".node = ");
            try self.serializeStep(&step);
            try self.write(",\n");
        } else {
            try self.write(".node = null,\n");
        }

        // Indent
        try self.writeIndent();
        try self.write(".indent = ");
        const indent_str = try std.fmt.allocPrint(self.allocator, "{}", .{cont.indent});
        defer self.allocator.free(indent_str);
        try self.write(indent_str);
        try self.write(",\n");

        // Continuations
        try self.writeIndent();
        try self.write(".continuations = &[_]Continuation{\n");
        self.indent();
        for (cont.continuations) |nested| {
            try self.serializeContinuation(&nested);
            try self.write(",\n");
        }
        self.dedent();
        try self.writeIndent();
        try self.write("},\n");

        // Location
        try self.writeIndent();
        try self.write(".location = ");
        try self.serializeSourceLocation(&cont.location);
        try self.write(",\n");
        
        self.dedent();
        try self.writeIndent();
        try self.write("}");
    }

    fn serializeStep(self: *AstSerializer, step: *const ast.Step) SerializeError!void {
        try self.writeIndent();
        switch (step.*) {
            .invocation => |invoc| {
                try self.write(".{ .invocation = ");
                try self.serializeInvocation(&invoc);
                try self.write(" }");
            },
            .label_apply => |label| {
                try self.write(".{ .label_apply = ");
                try self.writeString(label);
                try self.write(" }");
            },
            .label_with_invocation => |li| {
                try self.write(".{ .label_with_invocation = .{ .label = ");
                try self.writeString(li.label);
                try self.write(", .invocation = ");
                try self.serializeInvocation(&li.invocation);
                try self.write(", .is_declaration = ");
                if (li.is_declaration) {
                    try self.write("true");
                } else {
                    try self.write("false");
                }
                try self.write(" } }");
            },
            .label_jump => |lj| {
                try self.write(".{ .label_jump = .{ .label = ");
                try self.writeString(lj.label);
                try self.write(", .args = &[_]Arg{");
                for (lj.args, 0..) |arg, i| {
                    if (i > 0) try self.write(", ");
                    try self.write(".{ .name = ");
                    try self.writeString(arg.name);
                    try self.write(", .value = ");
                    try self.writeString(arg.value);
                    try self.write(" }");
                }
                try self.write("} } }");
            },
            .terminal => {
                try self.write(".{ .terminal = {} }");
            },
            .conditional_block => |cb| {
                try self.write(".{ .conditional_block = .{ .condition = ");
                if (cb.condition) |cond| {
                    try self.writeString(cond);
                } else {
                    try self.write("null");
                }
                try self.write(", .condition_expr = null, .nodes = &[_]Node{\n");
                self.indent();
                for (cb.nodes) |inner_step| {
                    try self.serializeStep(&inner_step);
                    try self.write(",\n");
                }
                self.dedent();
                try self.writeIndent();
                try self.write("}, .inserted_by_tap = ");
                try self.write(if (cb.inserted_by_tap) "true" else "false");
                try self.write(", .from_opaque_tap = ");
                try self.write(if (cb.from_opaque_tap) "true" else "false");
                try self.write(" } }");
            },
            .deref => |d| {
                try self.write(".{ .deref = .{ .target = ");
                try self.writeString(d.target);
                if (d.args) |args| {
                    try self.write(", .args = &[_]Arg{\n");
                    self.indent();
                    for (args) |arg| {
                        try self.serializeArg(&arg);
                        try self.write(",\n");
                    }
                    self.dedent();
                    try self.writeIndent();
                    try self.write("}");
                } else {
                    try self.write(", .args = null");
                }
                try self.write(" } }");
            },
            .branch_constructor => |bc| {
                try self.write(".{ .branch_constructor = ");
                try self.serializeBranchConstructor(&bc);
                try self.write(" }");
            },
            .metatype_binding => |mb| {
                try self.write(".{ .metatype_binding = .{ .metatype = ");
                try self.writeString(mb.metatype);
                try self.write(", .binding = ");
                try self.writeString(mb.binding);
                try self.write(", .source_event = ");
                try self.writeString(mb.source_event);
                try self.write(", .dest_event = ");
                if (mb.dest_event) |dest| {
                    try self.writeString(dest);
                } else {
                    try self.write("null");
                }
                try self.write(", .branch = ");
                try self.writeString(mb.branch);
                try self.write(", .inserted_by_tap = ");
                try self.write(if (mb.inserted_by_tap) "true" else "false");
                try self.write(", .from_opaque_tap = ");
                try self.write(if (mb.from_opaque_tap) "true" else "false");
                try self.write(" } }");
            },
            .inline_code => |code| {
                try self.write(".{ .inline_code = ");
                try self.writeString(code);
                try self.write(" }");
            },
            .foreach => |fe| {
                try self.write(".{ .foreach = .{ .iterable = ");
                try self.writeString(fe.iterable);
                try self.write(", .element_type = ");
                if (fe.element_type) |et| {
                    try self.writeString(et);
                } else {
                    try self.write("null");
                }
                try self.write(", .branches = &[_]NamedBranch{} } }");  // TODO: serialize branches
            },
            .conditional => |cond| {
                try self.write(".{ .conditional = .{ .condition = ");
                try self.writeString(cond.condition);
                try self.write(", .condition_expr = null, .branches = &[_]NamedBranch{} } }");  // TODO: serialize branches
            },
            .capture => |cap| {
                try self.write(".{ .capture = .{ .init_expr = ");
                try self.writeString(cap.init_expr);
                try self.write(", .branches = &[_]NamedBranch{} } }");  // TODO: serialize branches
            },
            .assignment => |asgn| {
                try self.write(".{ .assignment = .{ .target = ");
                try self.writeString(asgn.target);
                try self.write(", .fields = &[_]Field{} } }");  // TODO: serialize fields
            },
        }
    }

