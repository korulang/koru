        try self.write("\"body\": ");
        switch (subflow.body) {
            .flow => |flow| {
                try self.write("{\n");
                self.indent();
                try self.writeIndent();
                try self.write("\"type\": \"flow\",\n");
                try self.writeIndent();
                try self.write("\"flow\": {\n");
                self.indent();
                try self.serializeFlowJson(&flow);
                self.dedent();
                try self.writeIndent();
                try self.write("}\n");
                self.dedent();
                try self.writeIndent();
                try self.write("}");
            },
            .immediate => |imm| {
                try self.write("{\n");
                self.indent();
                try self.writeIndent();
                try self.write("\"type\": \"immediate\",\n");
                try self.writeIndent();
                try self.write("\"immediate\": ");
                try self.serializeBranchConstructorJson(&imm);
                try self.write("\n");
                self.dedent();
                try self.writeIndent();
                try self.write("}");
            },
        }
        try self.write(",\n");

        try self.writeIndent();
        try self.write("\"location\": ");
        try self.serializeLocationJson(&subflow.location);
    }

    fn serializeHostTypeDeclJson(self: *AstSerializer, host_type: *const ast.HostTypeDecl) !void {
        try self.writeIndent();
        try self.write("\"name\": ");
        try self.writeString(host_type.name);
        try self.write(",\n");

        try self.writeIndent();
        try self.write("\"shape\": ");
        try self.serializeShapeJson(&host_type.shape);
    }

    fn serializeDottedPathJson(self: *AstSerializer, path: *const ast.DottedPath) !void {
        try self.write("[");
        for (path.segments, 0..) |segment, i| {
            if (i > 0) try self.write(", ");
            try self.writeString(segment);
        }
        try self.write("]");
    }

    fn serializeShapeJson(self: *AstSerializer, shape: *const ast.Shape) !void {
        try self.write("{\n");
        self.indent();

        // Output fields array with full field information
        try self.writeIndent();
        try self.write("\"fields\": [\n");
        self.indent();

        for (shape.fields, 0..) |*field, i| {
            if (i > 0) try self.write(",\n");
            try self.writeIndent();
            try self.write("{\n");
            self.indent();

            // Field name
            try self.writeIndent();
            try self.write("\"name\": ");
            try self.writeString(field.name);
            try self.write(",\n");

            // Field type
            try self.writeIndent();
            try self.write("\"type\": ");
            if (field.module_path) |module_path| {
                // Output as "module.path:Type" for cross-module types
                const full_type = try std.fmt.allocPrint(self.allocator, "{s}:{s}", .{module_path, field.type});
                defer self.allocator.free(full_type);
                try self.writeString(full_type);
            } else {
                try self.writeString(field.type);
            }
            try self.write(",\n");

            // Special type flags
            try self.writeIndent();
            try self.write("\"is_source\": ");
            try self.write(if (field.is_source) "true" else "false");
            try self.write(",\n");

            try self.writeIndent();
            try self.write("\"is_file\": ");
            try self.write(if (field.is_file) "true" else "false");
            try self.write(",\n");

            try self.writeIndent();
            try self.write("\"is_embed_file\": ");
            try self.write(if (field.is_embed_file) "true" else "false");

            try self.write("\n");
            self.dedent();
            try self.writeIndent();
            try self.write("}");
        }

        try self.write("\n");
        self.dedent();
        try self.writeIndent();
        try self.write("]\n");

        self.dedent();
        try self.writeIndent();
        try self.write("}");
    }

    fn serializeBranchJson(self: *AstSerializer, branch: *const ast.Branch) !void {
        try self.writeIndent();
        try self.write("{\n");
        self.indent();

        try self.writeIndent();
        try self.write("\"name\": ");
        try self.writeString(branch.name);
        try self.write(",\n");

        try self.writeIndent();
        try self.write("\"payload\": ");
        try self.serializeShapeJson(&branch.payload);

        try self.write("\n");
        self.dedent();
        try self.writeIndent();
        try self.write("}");
    }

    fn serializeBranchConstructorJson(self: *AstSerializer, bc: *const ast.BranchConstructor) !void {
        try self.write("{\n");
        self.indent();

        try self.writeIndent();
        try self.write("\"branch_name\": ");
        try self.writeString(bc.branch_name);
        try self.write(",\n");

        try self.writeIndent();
        try self.write("\"fields\": [\n");
        self.indent();
        for (bc.fields, 0..) |*field, i| {
            if (i > 0) try self.write(",\n");
            try self.writeIndent();
            try self.write("{\n");
            self.indent();

            try self.writeIndent();
            try self.write("\"name\": ");
            try self.writeString(field.name);
            try self.write(",\n");

            try self.writeIndent();
            try self.write("\"type\": ");
            try self.writeString(field.type);
            try self.write(",\n");

            try self.writeIndent();
            try self.write("\"expression_str\": ");
            if (field.expression_str) |expr| {
                try self.writeString(expr);
            } else {
                try self.write("null");
            }

            try self.write("\n");
            self.dedent();
            try self.writeIndent();
            try self.write("}");
        }
        try self.write("\n");
        self.dedent();
        try self.writeIndent();
        try self.write("],\n");

        try self.writeIndent();
        try self.write("\"has_expressions\": ");
        try self.write(if (bc.has_expressions) "true" else "false");

        try self.write("\n");
        self.dedent();
        try self.writeIndent();
        try self.write("}");
    }

    fn serializeLocationJson(self: *AstSerializer, location: anytype) !void {
        try self.write("{\n");
        self.indent();

        try self.writeIndent();
        try self.write("\"file\": ");
        try self.writeString(location.file);
        try self.write(",\n");

        try self.writeIndent();
        try self.write("\"line\": ");
        const line_str = try std.fmt.allocPrint(self.allocator, "{d}", .{location.line});
        defer self.allocator.free(line_str);
        try self.write(line_str);
        try self.write(",\n");

        try self.writeIndent();
        try self.write("\"col\": ");
        const col_str = try std.fmt.allocPrint(self.allocator, "{d}", .{location.column});
        defer self.allocator.free(col_str);
        try self.write(col_str);

        try self.write("\n");
        self.dedent();
        try self.writeIndent();
        try self.write("}");
    }

    fn serializeSourceJson(self: *AstSerializer, source: *const ast.Source) !void {
        try self.write("{\n");
        self.indent();

        // Text
        try self.writeIndent();
        try self.write("\"text\": ");
        try self.writeString(source.text);
        try self.write(",\n");

        // Location
        try self.writeIndent();
        try self.write("\"location\": ");
        try self.serializeLocationJson(&source.location);
        try self.write(",\n");

        // Scope
        try self.writeIndent();
        try self.write("\"scope\": ");
        try self.serializeCapturedScopeJson(&source.scope);
        try self.write(",\n");

        // Phantom type
        try self.writeIndent();
        try self.write("\"phantom_type\": ");
        if (source.phantom_type) |pt| {
            try self.writeString(pt);
        } else {
            try self.write("null");
        }

        try self.write("\n");
        self.dedent();
        try self.writeIndent();
        try self.write("}");
    }

    fn serializeCapturedExpressionJson(self: *AstSerializer, expr: *const ast.CapturedExpression) !void {
        try self.write("{\n");
        self.indent();

        // Text
        try self.writeIndent();
        try self.write("\"text\": ");
        try self.writeString(expr.text);
        try self.write(",\n");

        // Location
        try self.writeIndent();
        try self.write("\"location\": ");
        try self.serializeLocationJson(&expr.location);
        try self.write(",\n");

        // Scope
        try self.writeIndent();
        try self.write("\"scope\": ");
        try self.serializeCapturedScopeJson(&expr.scope);

        try self.write("\n");
        self.dedent();
        try self.writeIndent();
        try self.write("}");
    }

    fn serializeCapturedScopeJson(self: *AstSerializer, scope: *const ast.CapturedScope) !void {
        try self.write("{\n");
        self.indent();

        try self.writeIndent();
        try self.write("\"bindings\": [\n");
        self.indent();
        for (scope.bindings, 0..) |*binding, i| {
            if (i > 0) try self.write(",\n");
            try self.serializeScopeBindingJson(binding);
        }
        try self.write("\n");
        self.dedent();
        try self.writeIndent();
        try self.write("]\n");

        self.dedent();
        try self.writeIndent();
        try self.write("}");
    }

    fn serializeScopeBindingJson(self: *AstSerializer, binding: *const ast.ScopeBinding) !void {
        try self.writeIndent();
        try self.write("{\n");
        self.indent();

        try self.writeIndent();
        try self.write("\"name\": ");
        try self.writeString(binding.name);
        try self.write(",\n");

        try self.writeIndent();
        try self.write("\"type\": ");
        try self.writeString(binding.type);
        try self.write(",\n");

        try self.writeIndent();
        try self.write("\"value_ref\": ");
        try self.writeString(binding.value_ref);

        try self.write("\n");
        self.dedent();
        try self.writeIndent();
        try self.write("}");
    }
};

